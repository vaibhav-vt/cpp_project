Map :: Map()
{
    soil = TextManager::LoadTexture("soil.png");
    grass = TextManager::LoadTexture("Grass.png");

    LoadMap(lvl1);

    src.x = 0;
    src.y = 0;
    src.w = 32;
    src.h = 32;

    dest.w = 32;
    dest.h = 32;
    
    dest.x = dest.y = 0;
}

Map :: ~Map()
{

}

void Map :: LoadMap(int arr[20][32])
{
    for(int i = 0;i < 20;i++)
    {
        for(int j = 0;j<32;j++)
        {
            map[i][j] = arr[i][j];
        }
    }
}

void Map :: DrawMap()
{
    int type = 0;
    for(int i = 0;i<20;i++)
    {
        for(int j = 0;j<32;j++)
        {
            type = map[i][j];

            dest.x = j*32;
            dest.y = i*32;

            if(type == 1)
            {
                TextManager::Draw(soil,src,dest);
            }
            if(type == 2)
            {
                TextManager::Draw(grass,src,dest);
            }

        }
    }
}

